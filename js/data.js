/* =========================================================
   Clean Edits — Shared data helpers
   Product catalog above; storage + utility helpers below.
   ========================================================= */

const STORAGE_KEYS = {
  cart: "ce_cart",
  wishlist: "ce_wishlist",
};

function formatPrice(n) {
  return "$" + Number(n).toFixed(2);
}

function getProductById(id) {
  return PRODUCTS.find((p) => p.id === id);
}

function getProductsByCategory(cat) {
  if (!cat) return PRODUCTS.slice();
  return PRODUCTS.filter((p) => p.category === cat);
}

function getRelatedProducts(product, count = 4) {
  return PRODUCTS.filter((p) => p.category === product.category && p.id !== product.id).slice(0, count);
}

function readStore(key) {
  try {
    const raw = localStorage.getItem(key);
    return raw ? JSON.parse(raw) : [];
  } catch (e) {
    return [];
  }
}

function writeStore(key, value) {
  try {
    localStorage.setItem(key, JSON.stringify(value));
  } catch (e) {
    /* storage unavailable — fail silently */
  }
}

/* ---------------- Cart ---------------- */
/* Cart item shape: { id, productId, color, size, qty } */

function getCart() {
  return readStore(STORAGE_KEYS.cart);
}

function saveCart(cart) {
  writeStore(STORAGE_KEYS.cart, cart);
  updateHeaderCounts();
}

function addToCart(productId, color, size, qty = 1) {
  const cart = getCart();
  const lineId = [productId, color, size].join("::");
  const existing = cart.find((i) => i.id === lineId);
  if (existing) {
    existing.qty += qty;
  } else {
    cart.push({ id: lineId, productId, color, size, qty });
  }
  saveCart(cart);
  return cart;
}

function updateCartQty(lineId, qty) {
  const cart = getCart();
  const item = cart.find((i) => i.id === lineId);
  if (item) {
    item.qty = Math.max(1, qty);
    saveCart(cart);
  }
  return cart;
}

function removeFromCart(lineId) {
  const cart = getCart().filter((i) => i.id !== lineId);
  saveCart(cart);
  return cart;
}

function clearCart() {
  saveCart([]);
}

function getCartCount() {
  return getCart().reduce((sum, i) => sum + i.qty, 0);
}

function getCartTotals() {
  const cart = getCart();
  let subtotal = 0;
  cart.forEach((item) => {
    const product = getProductById(item.productId);
    if (product) subtotal += product.price * item.qty;
  });
  const shipping = cart.length === 0 ? 0 : subtotal >= 150 ? 0 : 12;
  const tax = subtotal * 0.05;
  return { subtotal, shipping, tax, total: subtotal + shipping + tax };
}

/* ---------------- Wishlist ---------------- */

function getWishlist() {
  return readStore(STORAGE_KEYS.wishlist);
}

function saveWishlist(list) {
  writeStore(STORAGE_KEYS.wishlist, list);
  updateHeaderCounts();
}

function isWishlisted(productId) {
  return getWishlist().includes(productId);
}

function toggleWishlist(productId) {
  let list = getWishlist();
  if (list.includes(productId)) {
    list = list.filter((id) => id !== productId);
  } else {
    list.push(productId);
  }
  saveWishlist(list);
  return list.includes(productId);
}

/* ---------------- Header badge counts ---------------- */

function updateHeaderCounts() {
  const cartEl = document.getElementById("cartCount");
  const wishEl = document.getElementById("wishlistCount");
  if (cartEl) cartEl.textContent = getCartCount();
  if (wishEl) wishEl.textContent = getWishlist().length;
}
